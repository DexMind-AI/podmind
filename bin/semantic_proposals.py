#!/usr/bin/env -S uv run python
"""Generate merge proposals using embedding similarity instead of token jaccard.

Token jaccard catches `bitcoin-vs-ethereum ↔ bitcoin-and-ethereum` (same
tokens). Embeddings catch `regime-change-iran ↔ iran-overthrow-strategy`
(same meaning, zero token overlap).

Workflow:
    ./bin/embed_topics.py                   # one-shot or incremental
    ./bin/semantic_proposals.py --threshold 0.85
    # opens wiki/_merge-proposals.md with semantic candidates

Then the standard `--apply-from` flow on the file. Both jaccard and semantic
proposals write to the same checklist format, so the apply tool is unchanged.
"""
import argparse
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
import numpy as np

from podmind import embeddings
from _lib import WIKI_DIR

# Re-use the proposal-writer + canonical-picker from merge_topic_dups
sys.path.insert(0, str(Path(__file__).resolve().parent))
from merge_topic_dups import (  # noqa: E402
    PROPOSALS_PATH,
    PROPOSAL_HEADER,
    count_citations,
    pick_canonical,
)


def find_semantic_groups(threshold: float, top_k: int = 8) -> list[set[str]]:
    """Union-find over top-K embedding neighbors above `threshold`.

    For each topic, look at its top-K most similar OTHER topics; any pair
    with cosine ≥ threshold becomes a candidate edge. Groups are connected
    components of the resulting graph.
    """
    cached = embeddings.load("topics")
    n = len(cached.ids)
    if n == 0:
        return []
    idx, scores = embeddings.top_k_neighbors(cached.vecs, k=top_k)

    parent: dict[int, int] = {}
    def find(x: int) -> int:
        while parent.get(x, x) != x:
            parent[x] = parent.get(parent[x], parent[x])
            x = parent[x]
        return x
    def union(a: int, b: int) -> None:
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[ra] = rb

    for i in range(n):
        for j_pos, score in zip(idx[i], scores[i]):
            if score < threshold:
                continue
            parent.setdefault(i, i)
            parent.setdefault(int(j_pos), int(j_pos))
            union(i, int(j_pos))

    from collections import defaultdict
    groups_by_root: dict[int, set[str]] = defaultdict(set)
    for member in parent:
        root = find(member)
        groups_by_root[root].add(str(cached.ids[member]))
    return [g for g in groups_by_root.values() if len(g) > 1]


def write_proposals(groups: list[set[str]], threshold: float, mode: str) -> None:
    rel = PROPOSALS_PATH.relative_to(WIKI_DIR.parent)
    lines = [
        "# Merge proposals — review-then-apply",
        "",
        f"_Generated by `bin/semantic_proposals.py --threshold {threshold}` ({mode}-similarity)._",
        "",
        "**Reading the checkboxes**:",
        "- `[x] merge group` = approved",
        "- `[x] <slug>` = include in merge",
        "- `[ ] <slug>` = exclude from merge",
        "",
        f"When done, run `./bin/merge_topic_dups.py --apply-from {rel}`.",
        "",
        f"**Total proposals**: {len(groups)} semantic groups (cosine ≥ {threshold})",
        "",
        "---",
        "",
    ]
    sorted_groups = sorted(groups, key=lambda g: -sum(count_citations(s) for s in g))
    for i, group in enumerate(sorted_groups, 1):
        canonical = pick_canonical(group)
        cites = sorted(((s, count_citations(s)) for s in group), key=lambda x: -x[1])
        lines.append(f"## {i}. `{canonical}`\n")
        lines.append("- [ ] merge group")
        lines.append(f"- canonical: `{canonical}`")
        lines.append("- members:")
        for s, n in cites:
            tag = " ← canonical" if s == canonical else ""
            lines.append(f"  - [x] `{s}` ({n} citations){tag}")
        lines.append("")
    PROPOSALS_PATH.write_text("\n".join(lines))


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--threshold", type=float, default=0.85,
                    help="cosine threshold (0.85 = semantic-near; 0.92 = near-identical)")
    ap.add_argument("--top-k", type=int, default=8)
    args = ap.parse_args()

    groups = find_semantic_groups(args.threshold, top_k=args.top_k)
    print(f"Found {len(groups)} semantic groups (cosine ≥ {args.threshold})")
    if not groups:
        return
    write_proposals(groups, args.threshold, mode="semantic")
    rel = PROPOSALS_PATH.relative_to(WIKI_DIR.parent)
    print(f"→ wrote {rel} — open in Obsidian, review, then:")
    print(f"   ./bin/merge_topic_dups.py --apply-from {rel}")


if __name__ == "__main__":
    main()
