#!/usr/bin/env -S uv run python
"""merge_topic_ai — LLM verdict pass over the merge proposals.

For each near-dup group, asks the configured LLM provider: same-topic / different / partial?
Auto-ticks high-confidence "merge" groups, applies per-member exclusions for
"partial" groups, and annotates each group with a one-line AI rationale.
Low-confidence and "distinct" groups stay unticked for human review.

Usage:
  ./bin/merge_topic_ai.py --threshold 0.6 [--concurrency 12]

Reads/writes wiki/_merge-proposals.md (regenerated from scratch).
"""
import argparse
import asyncio
import json
import re
import sys
from pathlib import Path

import httpx

# Reuse the proposal-builder primitives
sys.path.insert(0, str(Path(__file__).parent))
from merge_topic_dups import find_pairs, union_find, pick_canonical, count_citations
from _lib import WIKI_DIR
from podmind import llm

PROPOSALS_PATH = WIKI_DIR / "_merge-proposals.md"


SYSTEM_PROMPT = """You are auditing near-duplicate topic slugs in a personal knowledge wiki.

Each input is a group of topic slugs that share token overlap and might be the same topic. Decide whether they should be merged.

Output ONE JSON object with exact fields:
{
  "verdict": "merge" | "partial" | "distinct" | "unsure",
  "canonical": "<slug from the input list>",
  "exclude": ["<slug>", ...],
  "reasoning": "<one sentence, ≤120 chars>"
}

Rules:
- "merge": all listed slugs are the same topic. `exclude` empty.
- "partial": most are the same topic but ≥1 are genuinely different. List the different ones in `exclude`.
- "distinct": these are different topics that just share words; do not merge. `exclude` empty.
- "unsure": ambiguous — needs a human. `exclude` empty.

When picking `canonical`, prefer the most general / most-cited / shortest form.

Examples:
  Input: ["iran-nuclear-program", "iran-nuclear-threat", "iran-nuclear-talks"]
  Output: {"verdict": "merge", "canonical": "iran-nuclear-program", "exclude": [], "reasoning": "All three describe Iran's nuclear program from different angles — same topic"}

  Input: ["bitcoin-mining-and-ai", "bitcoin-mining-and-energy"]
  Output: {"verdict": "distinct", "canonical": "bitcoin-mining-and-ai", "exclude": [], "reasoning": "AI integration vs energy economics are genuinely different aspects of bitcoin mining"}

  Input: ["claude-code", "claude-code-skills", "claude-code-plugin", "claude-code-bug"]
  Output: {"verdict": "partial", "canonical": "claude-code", "exclude": ["claude-code-bug"], "reasoning": "Skills and plugins are facets of claude-code; bug is a specific incident, keep separate"}
"""


def fmt_group(group: set[str]) -> tuple[list[tuple[str, int]], str]:
    cites = sorted(((s, count_citations(s)) for s in group), key=lambda x: -x[1])
    members_str = json.dumps([s for s, _ in cites])
    return cites, members_str


async def judge(client: httpx.AsyncClient, provider: llm.LLMProvider, group: set[str], sem: asyncio.Semaphore) -> dict:
    from podmind.llm_json import extract_json
    cites, members_str = fmt_group(group)
    # Combine system + user into a single prompt (provider.achat is single-turn)
    prompt = SYSTEM_PROMPT + "\n\n" + (
        f"Topic slugs (with citation counts):\n"
        + "\n".join(f"  - {s} ({n})" for s, n in cites)
        + "\n\nDecide. Output the JSON object only."
    )
    async with sem:
        for attempt in range(2):
            try:
                content, _ = await provider.achat(client, prompt, json_mode=True,
                                                  temperature=0.1, max_tokens=400,
                                                  timeout=60)
                parsed = extract_json(content)
                if parsed is not None:
                    return parsed
                # Fall through to retry
            except llm.LLMError:
                pass
            if attempt == 1:
                return {"verdict": "unsure", "canonical": pick_canonical(group),
                        "exclude": [], "reasoning": "AI call failed — needs manual review"}
            await asyncio.sleep(2)
    return {}


def write_proposals(groups: list[set[str]], verdicts: list[dict], threshold: float) -> tuple[int, int, int]:
    counts = {"merge": 0, "partial": 0, "distinct": 0, "unsure": 0}
    lines = [
        "# Merge proposals — review-then-apply",
        "",
        f"_Generated by `bin/merge_topic_ai.py --threshold {threshold}` with LLM verdicts._",
        "",
        "**AI verdicts** (each group has a `🤖` line):",
        "- `merge` — high-confidence same-topic; group pre-ticked, all members ticked-as-included",
        "- `partial` — same-topic except some members; group pre-ticked, AI-flagged exclusions left UNticked",
        "- `distinct` — AI thinks they're genuinely different; group left UNticked",
        "- `unsure` — AI couldn't decide; group left UNticked",
        "",
        "**Reading the checkboxes**:",
        "- `[x] merge group` = approved",
        "- `[x] <slug>` = include this slug in the merge",
        "- `[ ] <slug>` = exclude this slug from the merge",
        "",
        "**Note on Obsidian visuals**: Obsidian by default strikes-through ticked tasks. Drop `_obsidian-snippets/no-strikethrough.css` from this repo into your vault's `.obsidian/snippets/` and enable it in Settings → Appearance → CSS snippets if that's distracting.",
        "",
        f"When done, run `./bin/merge_topic_dups.py --apply-from {PROPOSALS_PATH.relative_to(WIKI_DIR.parent)}`.",
        "",
        f"**Total**: {len(groups)} groups | merge: {sum(1 for v in verdicts if v.get('verdict')=='merge')} | partial: {sum(1 for v in verdicts if v.get('verdict')=='partial')} | distinct: {sum(1 for v in verdicts if v.get('verdict')=='distinct')} | unsure: {sum(1 for v in verdicts if v.get('verdict')=='unsure')}",
        "",
        "---",
        "",
    ]
    sorted_pairs = sorted(zip(groups, verdicts), key=lambda gv: -sum(count_citations(s) for s in gv[0]))
    for i, (group, verdict) in enumerate(sorted_pairs, 1):
        v = verdict.get("verdict", "unsure")
        canonical = verdict.get("canonical") or pick_canonical(group)
        # Sanity: canonical must be in group
        if canonical not in group:
            canonical = pick_canonical(group)
        exclude = set(verdict.get("exclude", []) or []) & group
        reasoning = (verdict.get("reasoning") or "").strip().replace("\n", " ")[:140]
        counts[v if v in counts else "unsure"] += 1

        cites = sorted(((s, count_citations(s)) for s in group), key=lambda x: -x[1])
        lines.append(f"## {i}. `{canonical}`")
        lines.append("")
        lines.append(f"- 🤖 **{v}** — {reasoning}")
        # Group-level: pre-tick if AI says merge or partial
        group_mark = "x" if v in ("merge", "partial") else " "
        lines.append(f"- [{group_mark}] merge group")
        lines.append(f"- canonical: `{canonical}`")
        lines.append("- members (ticked = include, unticked = exclude):")
        for s, n in cites:
            tag = " ← canonical" if s == canonical else ""
            # AI-flagged exclusion: leave UNticked. Otherwise: tick (include).
            mark = " " if s in exclude else "x"
            lines.append(f"  - [{mark}] `{s}` ({n} citations){tag}")
        lines.append("")
    PROPOSALS_PATH.write_text("\n".join(lines))
    return counts["merge"], counts["partial"], counts["distinct"] + counts["unsure"]


async def main_async(threshold: float, concurrency: int) -> None:
    pairs = find_pairs(threshold)
    groups = union_find(pairs)
    print(f"[1/2] Found {len(pairs)} pairs → {len(groups)} groups (threshold {threshold})")

    provider = llm.get_provider()
    sem = asyncio.Semaphore(concurrency)
    verdicts: list[dict] = []

    print(f"[2/2] judging via LLM (concurrency={concurrency})...")
    async with httpx.AsyncClient() as client:
        # Preserve order
        sorted_groups = sorted(groups, key=lambda g: -sum(count_citations(s) for s in g))
        tasks = [judge(client, provider, g, sem) for g in sorted_groups]
        verdicts = await asyncio.gather(*tasks)

    n_merge, n_partial, n_skip = write_proposals(sorted_groups, verdicts, threshold)
    print()
    print(f"  merge   (auto-ticked): {n_merge}")
    print(f"  partial (auto-ticked, with excludes): {n_partial}")
    print(f"  distinct/unsure (human review):       {n_skip}")
    print(f"\n→ wrote {PROPOSALS_PATH}")


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--threshold", type=float, default=0.6)
    ap.add_argument("--concurrency", type=int, default=12)
    args = ap.parse_args()
    asyncio.run(main_async(args.threshold, args.concurrency))


if __name__ == "__main__":
    main()
